 
 -- DML UPDATE
 
 -- EXAMPLE 1
 
USE JFS15;

SELECT * FROM STUDENTS;

SET SQL_SAFE_UPDATES = 0;

UPDATE STUDENTS SET MOBILE = 100;

SELECT * FROM STUDENTS;

UPDATE STUDENTS SET ADDRESS = "ABC" WHERE SNO=1;

SELECT * FROM STUDENTS;

-- EXAMPLE 2

SELECT * FROM FINAL;

UPDATE FINAL SET MARKS = 100;

SELECT * FROM FINAL;

UPDATE FINAL SET STUDENT_NAME = "VARDHAN" WHERE ROLL = 3;

SELECT * FROM FINAL;

